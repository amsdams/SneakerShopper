package crawel.console;

public class Action {
	String action;

	String description;

	public String getAction() {
		return action;
	}

	public String getDescription() {
		return description;
	}

	public void setAction(String action) {
		this.action = action;
	}

	public void setDescription(String description) {
		this.description = description;
	}

}
